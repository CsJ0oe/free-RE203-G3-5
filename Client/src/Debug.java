
public class Debug {

    static public void error(String msg) {
    	System.err.println(msg);
    }

    static public void warning(String msg) {
    	System.err.println(msg);
    }

    static public void info(String msg) {
    	System.out.println(msg);
    }

    static public void debug(String msg) {
    	System.out.println(msg);
    }

}